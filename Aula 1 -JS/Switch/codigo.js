let opcao = parseInt(prompt("Digite um valor de 1 a 5: "));

switch (opcao) {
  case 1:
    alert("Você digitou 1");
    break;
  case 2:
    alert("Você digitou 2");
    break;
  case 3:
    alert("Você digitou 3");
    break;
  case 4:
    alert("Você digitou 4");
    break;
  case 5:
    break;
    alert("Você digitou 5");

  default:
    alert("Você digitou um valor invalido");
}
